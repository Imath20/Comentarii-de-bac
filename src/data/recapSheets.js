/** Nume afișat pentru curent (capitalizare) */
export function curentToLabel(curent) {
  if (!curent) return '';
  const map = {
    romantism: 'Romantism',
    realism: 'Realism',
    simbolism: 'Simbolism',
    modernism: 'Modernism',
    traditionalism: 'Traditionalism',
    postmodernism: 'Postmodernism',
    junimism: 'Junimism',
    lovinescu: 'E. Lovinescu',
  };
  return map[curent] || (curent.charAt(0).toUpperCase() + curent.slice(1));
}

